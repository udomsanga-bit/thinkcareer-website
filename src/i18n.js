// Simple context-free hook via a global store
window.TC_LANG = (function() {
  const listeners = new Set();
  // Reading localStorage can throw SecurityError in opaque-origin contexts
  // (file://, private mode, sandboxed iframes, some browsers). Guard it.
  let current = 'en';
  try {
    if (typeof localStorage !== 'undefined') {
      const stored = localStorage.getItem('tc-lang');
      if (stored) current = stored;
    }
  } catch (e) { /* opaque origin / private mode — ignore */ }
  return {
    get: () => current,
    set: (lang) => {
      current = lang;
      try { localStorage.setItem('tc-lang', lang); } catch (e) {}
      document.documentElement.setAttribute('lang', lang);
      document.documentElement.setAttribute('data-lang', lang);
      listeners.forEach(fn => fn(lang));
    },
    subscribe: (fn) => { listeners.add(fn); return () => listeners.delete(fn); },
    t: (key) => (window.TC_I18N[current] && window.TC_I18N[current][key]) || window.TC_I18N.en[key] || key,
  };
})();

